fix_css_simple() {
    local css_file="$1"
    if [ -f "$css_file" ]; then
        log "üîß –ü—Ä–æ–≤–µ—Ä—è–µ–º —Ñ–∞–π–ª: $css_file"
        
        # –°–æ–∑–¥–∞–µ–º –±—ç–∫–∞–ø —Ñ–∞–π–ª–∞
        cp "$css_file" "${css_file}.bak"
        
        # –ò—Å–ø—Ä–∞–≤–ª—è–µ–º —Ä–∞—Å–ø—Ä–æ—Å—Ç—Ä–∞–Ω–µ–Ω–Ω—ã–µ –ø—Ä–æ–±–ª–µ–º—ã:
        # 1. –£–±–∏—Ä–∞–µ–º –ª–∏—à–Ω–∏–µ –ø—É—Å—Ç—ã–µ —Å—Ç—Ä–æ–∫–∏ —Å —Ç–æ–ª—å–∫–æ –∑–∞–∫—Ä—ã–≤–∞—é—â–∏–º–∏ —Å–∫–æ–±–∫–∞–º–∏
        # 2. –£–±–∏—Ä–∞–µ–º –¥–≤–æ–π–Ω—ã–µ –∑–∞–∫—Ä—ã–≤–∞—é—â–∏–µ —Å–∫–æ–±–∫–∏
        # 3. –ò—Å–ø—Ä–∞–≤–ª—è–µ–º –Ω–µ–ø—Ä–∞–≤–∏–ª—å–Ω—ã–µ –≤–ª–æ–∂–µ–Ω–∏—è
        sed -i \
            -e '/^[[:space:]]*}[[:space:]]*$/N; s/\n[[:space:]]*}[[:space:]]*$//g' \
            -e 's/}[[:space:]]*}/}/g' \
            -e '/^[[:space:]]*}[[:space:]]*$/{N; /\n[[:space:]]*}[[:space:]]*$/d;}' \
            "$css_file" 2>/dev/null || {
            # –ï—Å–ª–∏ sed –Ω–µ —Å—Ä–∞–±–æ—Ç–∞–ª, –≤–æ—Å—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –∏–∑ –±—ç–∫–∞–ø–∞
            mv "${css_file}.bak" "$css_file"
            log "‚ö†Ô∏è –ù–µ —É–¥–∞–ª–æ—Å—å –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ –∏—Å–ø—Ä–∞–≤–∏—Ç—å $css_file, –æ—Å—Ç–∞–≤–ª—è–µ–º –æ—Ä–∏–≥–∏–Ω–∞–ª"
            return
        }
        
        # –£–¥–∞–ª—è–µ–º –±—ç–∫–∞–ø –µ—Å–ª–∏ –≤—Å–µ –ø—Ä–æ—à–ª–æ —É—Å–ø–µ—à–Ω–æ
